public class Location {

    private int x;
    private int y;

    /**
     * @param x the x position
     * @param y the y position
     */
    public Location(int x, int y) {
        this.x = x;
        this.y = y;
    }

    /**
     * @return the x position
     */
    public int getX() {
        return x;
    }

    /**
     * @return the y position
     */
    public int getY() {
        return y;
    }

}